medio = Turtle ()
medio.color("SteelBlue")
medio.speed(0.5)
medio.pensize(10)
medio.penup()
medio.goto(0,300)
medio.pendown()
# Linea punteada
radio = 275
pasos = 60
longitud_total = 2 * math.pi * radio
segmento = longitud_total / pasos
angulo = 360 / pasos
for I in range(pasos):
    medio.pendown()
    medio.forward(segmento / 2)
    medio.penup()
    medio.forward(segmento / 2)
    medio.right(angulo)

